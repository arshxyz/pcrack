let SUITE;
async function install() {
  if (
    (caches.delete("https://pixlr.com/x:v2"),
    caches.delete("https://pixlr.com/x-api:v2"),
    caches.delete("https://pixlr.com/x-template:v2"),
    caches.delete("https://pixlr.com/e:v2"),
    caches.delete("https://pixlr.com/e-api:v2"),
    caches.delete("https://pixlr.com/e-template:v2"),
    await offlineEnabled())
  ) {
    await caches.delete("suite");
    try {
      (SUITE = await caches.open("suite")),
        await SUITE.addAll([...bundles.suite, "/site.webmanifest"]);
    } catch {
      await caches.delete("suite");
    }
  }
}
async function offlineEnabled() {
  return await caches.has("suite");
}
function prefixLang(i, g) {
  return "us" === g || "" === g ? i : `/${g}${i}`;
}
function localizedPages(i) {
  return [
    prefixLang("/suite/", i),
    prefixLang("/batch/", i),
    prefixLang("/editor/", i),
    prefixLang("/express/", i),
  ];
}
self.addEventListener("install", (i) => {
  self.skipWaiting(), i.waitUntil(install());
}),
  self.addEventListener("activate", (i) => {
    i.waitUntil(clients.claim());
  }),
  self.addEventListener("fetch", (i) => {
    SUITE &&
      i.respondWith(fetch(i.request).catch((g) => SUITE.match(i.request)));
  }),
  self.addEventListener("message", async (i) => {
    const g = i.ports[0];
    if (g)
      if (i.data)
        switch ((console.info("[MESSAGE]", i.data), i.data.type)) {
          case "OFFLINE_READY": {
            const e = i.data.lang,
              s = await offlineEnabled();
            if (s) {
              const i = await caches.open("suite");
              if (!(await i.match(prefixLang("/suite/", e))))
                try {
                  await i.addAll(localizedPages(e));
                } catch (o) {
                  console.error("ServiceWorker:", o);
                }
            }
            g.postMessage(s);
            break;
          }
          case "OFFLINE_TOGGLE":
            if (i.data.value) {
              const g = i.data.lang;
              try {
                (SUITE = await caches.open("suite")),
                  await SUITE.addAll([
                    ...bundles.suite,
                    ...localizedPages(g),
                    "/site.webmanifest",
                  ]);
              } catch {
                await caches.delete("suite");
              }
            } else await caches.delete("suite"), (SUITE = void 0);
            g.postMessage(null);
            break;
          default:
            g.postMessage(
              new Error(
                `ServiceWorker: ${i.data.type} is not a valid message type`
              )
            );
        }
      else g.postMessage(new Error("ServiceWorker: Payload undefined"));
    else
      console.error(
        `ServiceWorker: Message ${i} cant be sent to ServiceWorker in this way, a port must be supplied for resposes`
      );
  });
